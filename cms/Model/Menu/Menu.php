<?php

namespace Cms\Model\Menu;
class Menu
{
    use \Engine\Core\Database\ActiveRecord;

    protected $table = 'menu';
    public $id;

    public $name;

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name): void
    {
        $this->name = $name;
    }

    

}